public class PrimitiveDataType {

    public static void main(String[] args) {

        int rollNo = 12;
        char letter = 'S';
        float mark = 95.62f;
        double largeDecimalNumber = 123456.9874;
        long largeNumber = 123456789101112L;
        boolean result = true;


//        byte and short are also primitive data type but use are very rare


        System.out.println("Mostly uses Primitive Data-type");

        System.out.println("rollNo :" + rollNo);
        System.out.println("letter :" + letter);
        System.out.println("mark :" + mark);
        System.out.println("largeDecimalNumber :" + largeDecimalNumber);
        System.out.println("largeNumber :" + largeNumber);
        System.out.println("result :" + result);
    }

}
